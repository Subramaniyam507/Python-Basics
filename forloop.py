import turtle
window=turtle.Screen

subbu=turtle.Turtle()

def squareturtle():
   
    subbu.forward(100)
    subbu.right(90)
    subbu.forward(100)
    subbu.right(90)
    subbu.forward(100)
    subbu.right(90)
    subbu.forward(100)
  
  

for i in range(4):
    
     squareturtle()
     
